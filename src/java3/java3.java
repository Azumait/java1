package java3;

public class java3 {

    public static void main(String[] args) {

    	// 반복문 (for, while)
        System.out.println("=== while ===");
        int i = 0;
        //..
        while(i < 3) {
            System.out.println(1);
//          i = i + 1;
            //..
            i++;
        }
        System.out.println("=== for ===");
        for(int j=0; j < 3; j++) {
            System.out.println(2);
        }

    }

}